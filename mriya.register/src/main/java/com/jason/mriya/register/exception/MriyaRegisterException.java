package com.jason.mriya.register.exception;

public class MriyaRegisterException extends RuntimeException {

	public MriyaRegisterException() {
		super();
	}

	public MriyaRegisterException(String message, Throwable cause) {
		super(message, cause);
	}

	public MriyaRegisterException(String message) {
		super(message);
	}

	public MriyaRegisterException(Throwable cause) {
		super(cause);
	}

	private static final long serialVersionUID = 1231232321231211L;

}
